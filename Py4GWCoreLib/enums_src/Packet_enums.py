CTO_OPCODES_BY_NAME: Dict[str, int] = {value: key for key, value in CTO_OPCODES.items()}


def get_opcode_name(header: int) -> str:
    name = CTO_OPCODES.get(header)
    if name:
        return name
    return f'0x{header:04X}'


def get_opcode_header(name: str) -> int:
    return CTO_OPCODES_BY_NAME.get(name, -1)
